#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   DisplayASCII
//  Description     :   Used to Display ASCII Values from Table
//  Input           :   Integer
//  Output          :   ASCII Values
//  Author          :   Prathamesh Rajesh Bawane
//  Date            :   01/12/2025
//
////////////////////////////////////////////////////////////////////////////////////////


void DisplayASCII()
{   

    for(int i = 0; i <= 255; i++)
    {
        if(i >= 32 && i <= 126)
        {
            printf("%3d\t\t%02X\t%03o\t%c\n",i,i,i,i);
        }
        else
        {
            printf("%3d\t\t%02X\t%03o\t.\n",i,i,i);
        }
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function : Main
//
////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    

     DisplayASCII();

    return 0;
}