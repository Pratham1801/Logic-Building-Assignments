void Reverse( char *str)
{   
    char *temp  = str;
   
    while(*str != '\0')
    {
        str++;
    }

    while(str >= temp)
    {
        printf("%c",*str);
        str--;
    }
    printf("\n");
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function : Main
//
////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    char arr[20];
    
    printf("Enter String :\n");
    scanf("%[^'\n]s",arr);

    Reverse(arr);
    
    return 0;
}